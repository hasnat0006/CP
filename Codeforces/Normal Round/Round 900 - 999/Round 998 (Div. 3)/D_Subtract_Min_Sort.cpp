void solve() {
    ll n;
    cin >> n;
    vector<ll> v(n);
    for (auto &i : v)
        cin >> i;
    for (int i = 0; i < n - 1; i++) {
        ll mn = min(v[i], v[i + 1]);
        v[i] -= mn, v[i + 1] -= mn;
    }
    dbg(v);
    cout << (is_sorted(vf(v)) ? "YES" : "NO") << '\n';
}

